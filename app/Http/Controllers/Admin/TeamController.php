     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $imageName = str_random(20) . '.' . $request->file('filename')->getClientOriginalExtension();
        $request->file('filename')->move(public_path('images/team'), $imageName);
        $data = [
            'name' => $request->get('name'),
            'avatar' => asset("images/team/{$imageName}")
        ];
        Team::create($data);

        return redirect()->back()->with('success', 'Stadium has been added');
    }

